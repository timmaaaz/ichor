    print(f"Config: {config['history_months']} months history, "
          f"{config['employee_count']} employees, {config['order_count']} orders")
    print(f"Products: {len(products['products'])} items loaded")
    print(f"Warehouses: {len(warehouses['warehouses'])} warehouses loaded")

    # Validate zone stages
    valid_stages = {"inbound", "received", "processing", "assembly",
                    "calibration", "qa", "outbound"}
    for wh in warehouses["warehouses"]:
        for zone in wh.get("zones", []):
            stage = zone.get("stage")
            if stage and stage not in valid_stages:
                raise ValueError(
                    f"Invalid zone stage '{stage}' in zone '{zone['name']}'. "
                    f"Valid: {valid_stages}")

    print("\nGenerating SQL files...")
    generate_org(config, warehouses, known_ids)
    generate_products(config, products, known_ids)
    generate_employees(config, known_ids)
    generate_inventory(config, products, warehouses, known_ids)
    generate_sales(config, products, known_ids)
    generate_procurement(config, products, suppliers, known_ids)
    generate_workflows(config, known_ids)
    generate_permissions(config, known_ids)

    print("\nDone.")


