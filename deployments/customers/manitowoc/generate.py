                f"  '{sp_id}',\n"
                f"  {sql_str(lot_number)},\n"
                f"  {sql_timestamp(mfg_date)},\n"
                f"  {sql_timestamp(exp_date)},\n"
                f"  {sql_timestamp(recv_date)},\n"
