        sup_id = supplier_ids[sup_idx]

        # Find supplier products for this supplier
        sup_prods = [(sp_id, psku, uc, pn) for sp_id, sid, psku, uc, pn in all_supplier_products if sid == sup_id]
        if not sup_prods:
            continue

